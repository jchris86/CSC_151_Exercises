/** This program uses the println method to display
   a line of from a film, as well as the character
   who said it, the title of the film, and the date
   the film was released */

public class MovieQuoteInfo
{
   public static void main(String[] args)
   {
	System.out.println("Quote: I'll make him an offer he can't refuse");
	System.out.println("Movie: The Godfather");
	System.out.println("Character: Vito Corleone");
	System.out.println("Date: 1972");
    }
}